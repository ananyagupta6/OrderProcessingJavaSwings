    private void btn_12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_12MouseClicked
        // TODO add your handling code here:
        CheckPass c = new CheckPass();
        if(c.Checkv(u.getText(),p.getText())){
            if(u.getText().isEmpty() && p.getText().isEmpty()) {
            jLabel5.setText("Username and phone cannot be empty");
            jLabel5.setVisible(true);
        }else if(pass.getText().isEmpty()) {
            jLabel5.setText("Password cannot be empty");
            jLabel5.setVisible(true);
            pass.setText("");
        } else if(pass1.getText().isEmpty()) {
            jLabel5.setText("Confirm password cannot be empty");
            jLabel5.setVisible(true);
            pass1.setText("");
        } else if(!pass.getText().equals(pass1.getText())) {
            jLabel5.setText("Password and Confirm password do not match");
            pass.setText("");
            pass1.setText("");
            jLabel5.setVisible(true);
        } else if((!((p.getText().replaceAll("[1234567890]", "")).isEmpty()) || !(p.getText().length() == 10))&& u.getText().isEmpty()) {
            jLabel5.setText("Phone number should be 10 digit numeric");
            jLabel5.setVisible(true);
        } 
        else if(pass.getText().length()>10){
                jLabel5.setText("Exceeding password length");
                jLabel5.setVisible(true);
            }
        else if(pass1.getText().length()>10){
                jLabel5.setText("Exceeding confirm password length");
                jLabel5.setVisible(true);
            } 
        else if(pass.getText().length()<=4){
                ForgotPass.InfoBox("WEAK PASSWORD !", " ALERT ");
                jLabel5.setText("Try some strong Password");
                jLabel5.setVisible(true);
            }
            else if(pass1.getText().length()<=4){
                ForgotPass.InfoBox("WEAK PASSWORD !", " ALERT ");
                jLabel5.setText("Try some strong Password");
                jLabel5.setVisible(true);
            }
        else {
            if(pass.getText().length()>=6){
                ForgotPass.InfoBox("YOUR PASSWORD IS STRONG!", " KUDOS ");
            }
            c.adddata(u.getText(),pass1.getText(),p.getText());
            c.addS(u.getText(),pass1.getText());
            jLabel5.setText("Password Updated SUCCESSFULLY ");
            jLabel5.setVisible(true);
            //c.vcount--;
            //c.See();
            
        }
        }
        else{
            jLabel5.setText("INVALID ACCOUNT.Please create an account first !");
            jLabel5.setVisible(true);
        }
    }//GEN-LAST:event_btn_12MouseClicked

